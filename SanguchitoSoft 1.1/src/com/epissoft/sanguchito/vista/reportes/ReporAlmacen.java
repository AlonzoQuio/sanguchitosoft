    private void BotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonActionPerformed
        if(CheckCategoria.isSelected()==true)
        {
          pasar.categoria=TextCategoria.getText();
          pasar.producto=" ";
          pasar.opcion=1;
        }
          
        
        if(CheckProducto.isSelected()==true)
        {
           
            pasar.producto=TextProducto.getText();
            pasar.categoria=" ";
            pasar.opcion=2;
            
        }
        if(CheckGeneral.isSelected()==true)
        {
            pasar.opcion=3;
        }
          
        dispose();
    }//GEN-LAST:event_BotonActionPerformed


