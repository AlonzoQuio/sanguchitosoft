    private void boton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardarActionPerformed
        // TODO add your handling code here:        
        UsuarioControlador ec = new UsuarioControlador();
        if(!text_pass1.getText().equals(text_pass2.getText())){
            JOptionPane.showMessageDialog(null, "Las contraseñas no soinciden");
        }
        else {
            JOptionPane.showMessageDialog(null, ec.actualizarUsuario(label_nombre.getText(),text_pass1.getText(), 
                    combo_tipo.getSelectedItem().toString()));
        }
    }//GEN-LAST:event_boton_guardarActionPerformed

    void setDNI(String dni, String nombre){
        label_dni.setText(dni);
        label_nombre.setText(nombre);
        UsuarioControlador ec = new UsuarioControlador();
        Usuario emp = ec.buscarUsuarioNom(nombre);
        ArrayList<String> datos = ec.getUsuarios(dni, nombre).get(0);      
    }
    
