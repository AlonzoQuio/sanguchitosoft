    private void inhabilitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inhabilitarActionPerformed
        // TODO add your handling code here:
        EmpleadoControlador ec = new EmpleadoControlador();
        if(!ec.buscarEmpleado(dni).getEmpEst()){
            JOptionPane.showMessageDialog(null, "El empleado ya esta Inhabilitado");
        }
        else{
            dispose();
            InHabilitarEmpleado mod = new InHabilitarEmpleado();
            mod.setDNI(dni);
            mod.setLocation(Sanguchito.sanguchito.getWidth()/2-mod.getWidth()/2,Sanguchito.sanguchito.getHeight()/2-mod.getHeight()/2);
            Sanguchito.sanguchito.add(mod, new Integer(15));
            mod.show();
        }
    }//GEN-LAST:event_inhabilitarActionPerformed

    private void habilitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_habilitarActionPerformed
        // TODO add your handling code here:
        EmpleadoControlador ec = new EmpleadoControlador();
        if(ec.buscarEmpleado(dni).getEmpEst()){
            JOptionPane.showMessageDialog(null, "El empleado esta Habilitado");            
        }
        else{
            dispose();
            HabilitarEmpleado mod = new HabilitarEmpleado();
            mod.setDNI(dni);
            mod.setLocation(Sanguchito.sanguchito.getWidth()/2-mod.getWidth()/2,Sanguchito.sanguchito.getHeight()/2-mod.getHeight()/2);
            Sanguchito.sanguchito.add(mod, new Integer(15));
            mod.show();
        }
    }//GEN-LAST:event_habilitarActionPerformed

