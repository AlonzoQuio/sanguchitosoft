    private void buscar_empleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar_empleadoActionPerformed
         // TODO add your handling code here:
        /*
        Limpiamos la tabla
        */
        try {
            DefaultTableModel modelo=(DefaultTableModel) tabla_empleados.getModel();
            int filas = tabla_empleados.getRowCount();
            for (int i = 0;filas>i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            System.out.println("Error al limpiar la tabla.");
        }        
        ArrayList<ArrayList<String>> usuarios;
        String dni = text_dni.getText();
        String apellido = text_apellido.getText();
        UsuarioControlador ec = new UsuarioControlador();
        usuarios = ec.getUsuarios(dni, apellido);
        
        DefaultTableModel tabla = (DefaultTableModel)tabla_empleados.getModel();
        int n = usuarios.size();
        for( int i = 0; i < n; i++){
            usuarios.get(i).add("Ajustar");
            tabla.addRow(usuarios.get(i).toArray());           
        }
        Action action = new AbstractAction() {

            @Override
            public void actionPerformed(ActionEvent e) {
                int select = tabla_empleados.getSelectedRow();
                String dni = (String)tabla_empleados.getValueAt(select, 0);
                String nombre = (String)tabla_empleados.getValueAt(select, 1);
                OpcionesUsuario opciones = new OpcionesUsuario();
                opciones.setDNI(dni, nombre);
                opciones.setLocation(Sanguchito.sanguchito.getWidth()/2-opciones.getWidth()/2,Sanguchito.sanguchito.getHeight()/2-opciones.getHeight()/2);
                Sanguchito.sanguchito.add(opciones, new Integer( 10 ));
                opciones.show();
            }
        };
        ButtonColumn b = new ButtonColumn(tabla_empleados, action, 6);
    }//GEN-LAST:event_buscar_empleadoActionPerformed

