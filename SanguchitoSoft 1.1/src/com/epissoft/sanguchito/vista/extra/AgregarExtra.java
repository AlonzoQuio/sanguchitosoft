    private void bAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarActionPerformed
        if(jTImagen.getText().length()<2){
            JOptionPane.showMessageDialog(null,"Debe seleccionar una imagen","Error",JOptionPane.ERROR_MESSAGE);
        }else{
            String resultado=ExtraControlador.agregarExtra(tNombre.getText(),tPrecFijo.getText(),tPrecVenta.getText(),categoria.getCatExtCod());
            if(resultado.equals("CORRECTO")){
                ImageResizer.copyImage(jTImagen.getText(),ManejoSesion.getRuta()+"/Imagenes/","ext"+tNombre.getText(),60,60);
                try {
                    Thread.sleep(500);
                } catch (InterruptedException ex) {
                }
                atras();
            }else{
                JOptionPane.showMessageDialog(null, resultado, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_bAgregarActionPerformed

