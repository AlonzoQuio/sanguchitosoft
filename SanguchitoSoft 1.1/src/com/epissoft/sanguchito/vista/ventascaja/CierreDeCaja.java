    public CierreDeCaja() {
        initComponents();
        int inicio=AperturaCajaControlador.inicioDocumento();
        
        CargarFecha.cargarFechaActual(tFecha);
        tEncargado.setText(ManejoSesion.getUsuario());
        Calendar fecha = Calendar.getInstance();
        tHora.setText(""+fecha.getTime().getHours()+":"+fecha.getTime().getMinutes());
        
        List<Facturacion> list = FacturacionControlador.facturasPeriodo(inicio,FacturacionControlador.facturaAcual());
        DefaultTableModel tabla = (DefaultTableModel)tablaDetalle.getModel();
        float monto=0;
        for(Facturacion iter : list){
            Object[]data ={""+iter.getFacCod(),""+iter.getFacFecDia()+"/"+iter.getFacFecMes()+"/"+iter.getFacFecAnio(),
                ""+iter.getFacHor().getHours()+":"+iter.getFacHor().getMinutes(),
                iter.getFacTot()};
            tabla.addRow(data);
            monto+=iter.getFacTot();
        }
        tablaDetalle.setModel(tabla);
        tMonto.setText(""+monto);
        float montoAp=AperturaCajaControlador.montoApertura();
        tApertura.setText(""+montoAp);
        tTotal.setText(""+(monto+montoAp));
    }

