    private void bAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarActionPerformed
        
        String resultado=CategoriaProductoControlador.actualizarCategoriaProducto(categoria.getCatProdCod(),tNombre.getText(),"catProd"+tNombre.getText()+".png");
        if(resultado.equals("CORRECTO")){
            ImageResizer.copyImage(jTImagen.getText(),ManejoSesion.getRuta()+"/Imagenes/","catProd"+tNombre.getText(),60,60);
            try {
                Thread.sleep(500);
            } catch (InterruptedException ex) {
            }
            atras();
        }else{
            JOptionPane.showMessageDialog(null,resultado);
        }
    }//GEN-LAST:event_bAgregarActionPerformed

